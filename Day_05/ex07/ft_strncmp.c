int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;

	i = 0;
	if (*s1 == '\0' || *s2 == '\0')
		return (*s1 - *s2);
	while (*s1++ == *s2++ && !(*s1 == '\0' && *s2 == '\0') && i++ < n)
		;
	return (*(s1 - 1) - *(s2 - 1));
}
