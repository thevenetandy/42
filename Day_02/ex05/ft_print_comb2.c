void	ft_putchar(char c);

void	ft_print_comb2(void)
{
	int n[2];

	n[0] = -1;
	n[1] = 0;
	while (n[0]++ <= 99)
	{
		n[1] = n[0];
		while (++n[1] <= 99)
		{
			ft_putchar('0' + n[0] / 10);
			ft_putchar('0' + n[0] % 10);
			ft_putchar(' ');
			ft_putchar('0' + n[1] / 10);
			ft_putchar('0' + n[1] % 10);
			if (n[0] < 98)
			{
				ft_putchar(',');
				ft_putchar(' ');
			}
		}
	}
}
