int	ft_is_prime(int nb)
{
	int i;
	int j;

	i = 5;
	if (nb == 2 || nb == 3)
		return (1);
	if (nb <= 1 || nb % 2 == 0 || nb % 3 == 0)
		return (0);
	while (i * i <= nb)
	{
		if (nb % i == 0)
			return (0);
		i += j;
		j = 6 - j;
	}
	return (1);
}
