void	ft_putchar(char c);

void	ft_print_comb(void)
{
	char n[3];

	n[0] = '0';
	n[1] = '1';
	n[2] = '2';
	while (n[0] <= '7')
	{
		while (n[1] <= '8')
		{
			while (n[2] <= '9')
			{
				ft_putchar(n[0]);
				ft_putchar(n[1]);
				ft_putchar(n[2]);
				if (n[2]++ != '9' || n[0] != '7')
				{
					ft_putchar(',');
					ft_putchar(' ');
				}
			}
			n[1]++;
			n[2] = n[1] + 1;
		}
		n[1] = ++n[0];
	}
}
