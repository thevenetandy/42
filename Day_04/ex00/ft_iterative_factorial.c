int	ft_iterative_factorial(int nb)
{
	int f;
	int i;

	f = 1;
	i = 1;
	if (nb < 0 || nb > 12)
		return (0);
	if (nb == 0)
		return (1);
	while (i++ < nb)
		f *= i;
	return (f);
}
